export const localeOptions: { value: Locale; label: string; dir: Direction }[] = [
  { value: "en", label: "English", dir: "ltr" },
  { value: "ar", label: "العربية", dir: "rtl" },
  { value: "zh", label: "中文", dir: "ltr" },
  { value: "ru", label: "Русский", dir: "ltr" },
  { value: "ur", label: "اردو", dir: "rtl" },
  { value: "tr", label: "Türkçe", dir: "ltr" },
];

export function getDirection(locale: Locale): Direction {
  return localeOptions.find(option => option.value === locale)?.dir ?? "ltr";
}

export function isSupportedLocale(value: unknown): value is Locale {
  return (
    typeof value === "string" &&
    localeOptions.some(option => option.value === value)
  );
}

export function t(locale: Locale, key: TranslationKey): string {
  return translations[locale]?.[key] ?? english[key];
}

export function translateUiLabel(locale: Locale, label?: string): string {
  if (!label) return "";
  return uiLabels[label]?.[locale] ?? label;
}
